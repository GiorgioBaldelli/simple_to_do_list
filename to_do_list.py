todo_list = list()

print("What should we remember doing?")
print("Enter 'DONE' to stop adding activities.")

while True:
  new_item = input("> ")
  
  if new_item == 'DONE':
    break
    
  todo_list.append(new_item)
  print("Added! List has {} activities to be completed.".format(len(todo_list)))
  continue
        
print("Here's your list:")
  
for item in todo_list:
  print(item)
